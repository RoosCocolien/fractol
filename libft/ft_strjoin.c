#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		len;
	char	*fresh;

	i = 0;
	j = 0;
	len = (int)ft_strlen((char*)s1) + (int)ft_strlen((char*)s2);
	fresh = (char*)ft_memalloc(len + 1);
	if (!(fresh))
		return (NULL);
	while (s1[i])
	{
		fresh[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		fresh[i] = s2[j];
		i++;
		j++;
	}
	fresh[i] = '\0';
	return (fresh);
}
